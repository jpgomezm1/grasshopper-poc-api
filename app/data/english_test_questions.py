def get_questions_for_client():
    """Return questions without correct answers for the frontend."""
    result = []
    current_passage = None
    for q in ENGLISH_TEST_QUESTIONS:
        item = {
            "id": q["id"],
            "section": q["section"],
            "difficulty": q["difficulty"],
            "question": q["question"],
            "options": q["options"],
        }
        if q.get("passage"):
            current_passage = q["passage"]
            item["passage"] = current_passage
        elif q["section"] == "reading" and q.get("passage") is None and current_passage:
            item["passage"] = current_passage
        result.append(item)
    return result


def calculate_score(answers: dict) -> dict:
    """Calculate test score and CEFR level from user answers."""
    question_map = {q["id"]: q for q in ENGLISH_TEST_QUESTIONS}

    section_correct = {"grammar": 0, "vocabulary": 0, "reading": 0}
    section_total = {"grammar": 0, "vocabulary": 0, "reading": 0}
    total_correct = 0

    for q in ENGLISH_TEST_QUESTIONS:
        section_total[q["section"]] += 1
        user_answer = answers.get(q["id"])
        if user_answer == q["correct"]:
            total_correct += 1
            section_correct[q["section"]] += 1

    total_questions = len(ENGLISH_TEST_QUESTIONS)
    percentage = (total_correct / total_questions) * 100 if total_questions > 0 else 0

    if percentage >= 81:
        cefr = "C1"
    elif percentage >= 66:
        cefr = "B2"
    elif percentage >= 51:
        cefr = "B1"
    elif percentage >= 31:
        cefr = "A2"
    else:
        cefr = "A1"

    section_scores = {}
    for section in section_correct:
        total = section_total[section]
        correct = section_correct[section]
        section_scores[section] = {
            "correct": correct,
            "total": total,
            "percentage": round((correct / total) * 100) if total > 0 else 0,
        }

    return {
        "score": total_correct,
        "total_questions": total_questions,
        "percentage": round(percentage),
        "cefr_level": cefr,
        "section_scores": section_scores,
    }
